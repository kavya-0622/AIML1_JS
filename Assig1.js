// 1. Program to demonstrate assignment operators

let num = 10;

console.log("Initial value:", num);

// += operator
num += 5; 
console.log("After += 5:", num);

// -= operator
num -= 3;
console.log("After -= 3:", num);

// *= operator
num *= 2;
console.log("After *= 2:", num);

// /= operator
num /= 4;
console.log("After /= 4:", num);

// %= operator
num %= 3;
console.log("After %= 3:", num);